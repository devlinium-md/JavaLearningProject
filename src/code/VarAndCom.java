package code;

public class VarAndCom {
    public static void main(String[] args){
        //Declare some variables

        boolean boolVar = false;
        boolean bool2ndVar = true;

        char charVar = 'a';
        char char2mdVar = 123;

        byte byteVar = 1;

        short someShortVar = 248;

        int someNumbersVar = 334;
        int otherNumbersVar = -6741;

        long bigNumberVar = 123342;
        float numberWithDecimalsVar = 3.5F;
        double numberWithMoreDecimalsVar = 3.5;

        System.out.println("boolVar = " + boolVar);
        System.out.println("bool2ndVar = " + bool2ndVar);
        System.out.println("charVar = " + charVar);
        System.out.println("char2mdVar = " + char2mdVar);
        System.out.println("byteVar = " + byteVar);
        System.out.println("someShortVar = " + someShortVar);
        System.out.println("someNumbersVar = " + someNumbersVar);
        System.out.println("otherNumbersVar = " + otherNumbersVar);
        System.out.println("bigNumberVar = " + bigNumberVar);
        System.out.println("numberWithDecimalsVar = " + numberWithDecimalsVar);
        System.out.println("numberWithMoreDecimalsVar = " + numberWithMoreDecimalsVar);
    }
}
